package modelo;

public class Pedido {

	    private int Numero;
	    private int Dataida;
	    private int Datavolta;
		public int getNumero() {
			return Numero;
		}
		public void setNumero(int numero) {
			Numero = numero;
		}
		public int getDataida() {
			return Dataida;
		}
		public void setDataida(int dataida) {
			Dataida = dataida;
		}
		public int getDatavolta() {
			return Datavolta;
		}
		public void setDatavolta(int datavolta) {
			Datavolta = datavolta;
		}
}
